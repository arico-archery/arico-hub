// 자사몰(MakeShop) 옵션 그룹 이름이 잘못 지어진 경우를 표시 단계에서 교정한다.
//
// 왜 필요한가: 주문 옵션은 MakeShop의 variationName("カラー : 46 サイズ : L")을 그대로 받는다.
// 그런데 リム(limb) 상품은 파운드(38·46 등)를 고르는데 자사몰 옵션 그룹 이름이 「カラー」로
// 되어 있다. 그대로 두면 주문서·청구서에 "색상 46"이라고 나가 담당자도 고객도 헷갈린다.
//
// 판별 근거: 색상 값이 순수 숫자인 경우는 없다(ブラック·レッド…). 반대로 파운드는 늘 숫자다.
// 실제 데이터에서도 「カラー : 숫자」는 전부 リム 상품이었고, 올바른 「ポンド」 축이 따로 존재한다.
//
// 근본 해결은 자사몰에서 옵션 그룹 이름을 고치는 것이다. 고쳐지면 이 함수는 아무것도 바꾸지 않는다.
export function fixOptionAxes(label: string | null | undefined): string {
  const s = String(label || '')
  if (!s) return ''
  // 「カラー : 46」「カラー : 12ポンド」 → 「ポンド : …」
  //   값이 숫자로 시작하면 색상이 아니다. 뒤에 단위(ポンド/#/lb)가 붙는 경우도 있다.
  //   다음 축(「サイズ :」 등)이나 문자열 끝까지를 값으로 본다.
  return s.replace(
    /(カラー|カラ－|color)(\s*[:：]\s*)(\d+(?:\.\d+)?\s*(?:ポンド|ポンド|#|lbs?)?)(?=\s|$)/gi,
    'ポンド$2$3',
  )
}
